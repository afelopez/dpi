function [ label_str ] = ntostr( label )
    if label == 1
            label_str = 'uno';
        elseif label == 2
            label_str = 'dos';
        elseif label == 3
            label_str = 'tres';
        elseif label == 4
            label_str = 'cuatro';
        elseif label == 5
            label_str = 'cinco';
    end
end

